package web.model;

public class User {
    public String model;
    private String color;
    private int year;


    public User() {
        this.model = model;
        this.color = color;
        this.year = year;

    }

    public String getModel() {
        return model;
    }

    public String getColor() {
        return color;
    }

    public int getYear() {
        return year;
    }


}
